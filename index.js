function getGRshortdate() {
    let monthGR = ['Ιαν', 'Φεβ', 'Μαρ', 'Απρ', 'Μαΐ', 'Ιουν', 'Ιουλ', 'Αυγ', 'Σεπ', 'Οκτ', 'Νοε', 'Δεκ'];

    let cdate = new Date();
    
    let month = monthGR[cdate.getMonth()];
    let year = cdate.getFullYear();
   
    /*  εδω θα δωσουμε τη μορφη της ημερομηνιας οπως την θελουμε για τις διαρκειες ετους Εργασιες / Δεξιοτητες / Σπουδές*/
    return month + '-' + year;
   
}

